/*:
 ## Alıştırma - Ternary Operatör
 
 
 Aşağıdaki kodu, ternary operatör kullanarak `enBüyük` değişkeni tek satırda tanımlı olacak şekilde yeniden düzenleyin.
 */
let sayı1 = 14
let sayı2 = 25

var enBüyük: Int
if sayı1 > sayı2 {
    enBüyük = sayı1
} else {
    enBüyük = sayı2
}

